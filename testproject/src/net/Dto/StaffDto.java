package net.Dto;

public class StaffDto {
	private Integer No;
	private String Name;
	private String Sn;
	private String Graduateday;
	private String SchoolNo;
	private String ReligionNo;
	public Integer getNo() {
		return No;
	}
	public void setNo(Integer no) {
		No = no;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getSn() {
		return Sn;
	}
	public void setSn(String sn) {
		Sn = sn;
	}
	public String getGraduateday() {
		return Graduateday;
	}
	public void setGraduateday(String graduateday) {
		Graduateday = graduateday;
	}
	public String getSchoolNo() {
		return SchoolNo;
	}
	public void setSchoolNo(String schoolNo) {
		SchoolNo = schoolNo;
	}
	public String getReligionNo() {
		return ReligionNo;
	}
	public void setReligionNo(String religionNo) {
		ReligionNo = religionNo;
	}
	
}
